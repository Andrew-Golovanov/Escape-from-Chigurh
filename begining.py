def main_menu():  # Показывает главное меню
    while True:  # Бесконечный цикл, пока не выберем один из пунктов
        clear()
        print("╔═══════════════════════════════════════════════════════════╗")
        print("║                      ГЛАВНОЕ МЕНЮ                         ║")
        print("╚═══════════════════════════════════════════════════════════╝")
        print()
        print("1. Новая игра")
        print("2. Продолжить")
        print("3. Удалить сохранение")
        print("4. Выход")
        print()

        save_chapter = check_save()  # Проверяем наличие сохранения и показываем информацию
        if save_chapter:
            print(f"💾 Сохранение найдено: {save_chapter}")
        else:
            print("❌ Нет сохранений")
        print()

        menu_choice = input("Выберите пункт: ")  # Выбор пользователя

        if menu_choice == '1':  # Новая игра
            clear()
            print("╔═══════════════════════════════════════════════════════════╗")
            print("║               НАПИШИ ИМЯ СВОЕГО ПЕРСОНАЖА                 ║")
            print("╚═══════════════════════════════════════════════════════════╝")

            character_name = input("  ").strip()  # Имя персонажа

            if character_name == "":  # Если имя не введено, используем имя по умолчанию
                character_name = "Ллевелин Мосс"

            save_character_name(character_name)  # Сохраняем имя в файл

            clear()
            time.sleep(0.5)
            print("╔═══════════════════════════════════════════════════════════╗")
            print("║              ✅ ИМЯ УСПЕШНО СОХРАНЕНО!                    ║")
            print("╚═══════════════════════════════════════════════════════════╝")
            time.sleep(1)
            print(f"╔═══════════════════════════════════════════════════════════╗")
            print(f"║  Персонаж: {character_name.center(47)}║")
            print(f"╚═══════════════════════════════════════════════════════════╝")
            time.sleep(1.5)
            clear()

            delete_save()  # Удаляем старое сохранение если оно есть

            subprocess.run([sys.executable, "plot.py", "new"])  # Запускаем сюжет (plot.py) в режиме новой игры
            break

        elif menu_choice == '2':  # Продолжить
            save_chapter = check_save()
            if save_chapter:  # Если сохранение существует
                clear()
                print("╔═══════════════════════════════════════════════════════════╗")
                print("║                   ЗАГРУЗКА СОХРАНЕНИЯ...                  ║")
                print("╚═══════════════════════════════════════════════════════════╝")
                time.sleep(1.5)
                print(f"💾 Глава: {save_chapter}")
                time.sleep(1.5)
                clear()
                # Запускаем сюжет (plot.py) в режиме продолжения
                subprocess.run([sys.executable, "plot.py", "continue"])
                break
            else:  # Если сохранения нет
                clear()
                print("╔═══════════════════════════════════════════════════════════╗")
                print("║              ❌ НЕТ СОХРАНЕНИЙ!                           ║")
                print("╚═══════════════════════════════════════════════════════════╝")
                time.sleep(2)
                clear()

        elif menu_choice == '3':  # Удалить сохранение
            delete_save()
            clear()
            print("╔═══════════════════════════════════════════════════════════╗")
            print("║              ✅ СОХРАНЕНИЕ УДАЛЕНО!                       ║")
            print("╚═══════════════════════════════════════════════════════════╝")
            time.sleep(2)
            clear()

        elif menu_choice == '4':  # Выход из игры
            clear()
