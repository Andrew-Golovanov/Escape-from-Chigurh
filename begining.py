import time
import os
import subprocess
import sys


def clear():  # Очищает экран консоли
    os.system('cls' if os.name == 'nt' else 'clear')


def load_character_name():  # Загружает имя персонажа из файла character_name.txt
    if os.path.exists("character_name.txt"):
        with open("character_name.txt", "r", encoding="utf-8") as file:  # Открываем файл для чтения
            name = file.read().strip()
            if name:
                return name
    return "Ллевелин Мосс"


def save_character_name(name):  # Сохраняет имя персонажа в файл character_name.txt
    with open("character_name.txt", "w", encoding="utf-8") as file:
        file.write(name)


def check_save():  # Проверяет наличие сохранения и возвращает название главы
    if os.path.exists("save.txt"):
        with open("save.txt", "r", encoding="utf-8") as file:
            lines = file.readlines()
            if len(lines) >= 1:
                return lines[0].strip()
    return None


def delete_save():  # Удаляет файл сохранения если он существует
    if os.path.exists("save.txt"):
        os.remove("save.txt")


