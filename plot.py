def load_game():
    try:
        with open("save.txt", "r", encoding="utf-8") as file:
            lines = file.readlines()
            if len(lines) >= 2:  # Проверяем, что есть хотя бы 2 строки
                chapter = lines[0].strip()
                player.load_data(lines[1].strip())
                return chapter  # Возвращаем главу
    except:
        pass
    return None  # Если сохранения нет


